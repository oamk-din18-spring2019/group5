Spider-Man 3 is a 2007 American superhero film based on the fictional Marvel Comics character Spider-Man. <br>
 It was directed by Sam Raimi from a screenplay by Raimi, his older brother Ivan, and Alvin Sargent. <br>
  It is the third and final installment in Raimi's original Spider-Man film trilogy, following Spider-Man (2002) and Spider-Man 2 (2004). <br> <br>
Genres: Action, Drama, Fantasy, Science Fiction, Thriller <br>
